const getLastSunday = () => {
  const today = new Date();
  const dayOfWeek = today.getDay(); // Sunday is 0
  const lastSunday = new Date(today);
  lastSunday.setDate(today.getDate() - dayOfWeek); // Move back to last Sunday
  lastSunday.setHours(0, 0, 0, 0); // Start of the day
  return lastSunday;
};

export const totalWeeklyHours = () => {
  const lastSunday = getLastSunday();
  const nextSaturday = new Date(lastSunday);
  nextSaturday.setDate(lastSunday.getDate() + 6);

  const holiday = events.filter((event) => {
    const eventDate = new Date(event.date);
    return (
      eventDate >= lastSunday &&
      eventDate <= nextSaturday &&
      eventDate.getDay() !== 0 &&
      eventDate.getDay() !== 1 &&
      event.values.some((value) => value.status === "official")
    );
  });

  if (holiday) {
    return 42 - 8.4 * holiday.length;
  }
  return 42;
};
export const totalWeeklyHoursInLastWeek = () => {
  const lastLastSunday = new Date(getLastSunday());
  lastLastSunday.setDate(getLastSunday().getDate() - 7);
  const lastSaturday = new Date(getLastSunday());
  lastSaturday.setDate(getLastSunday().getDate() - 1);

  const holiday = events.filter((event) => {
    const eventDate = new Date(event.date);
    return (
      eventDate >= lastLastSunday &&
      eventDate <= lastSaturday &&
      eventDate.getDay() !== 0 &&
      eventDate.getDay() !== 1 &&
      event.values.some((value) => value.status === "official")
    );
  });

  if (holiday) {
    return 42 - 8.4 * holiday.length;
  }
  return 42;
};

